int		ft_count_if(char **tab, int (*f)(char*))
{
	int cptr;
	int i;

	cptr = 0;
	i = 0;
	while (tab[i] != '\0')
	{
		if (f(tab[i]) == 1)
		{
			cptr++;
		}
		i++;
	}
	return (cptr);
}
